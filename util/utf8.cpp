int utf8_digit_value(rune digit) {
    int range = find(decimal_digits, (sizeof(decimal_digits)) / sizeof(range), digit);
	if (range == -1) { // Not a digit
		ERROR = INVALID_RUNE;
		return -1;
	}
	return digit - decimal_digits[range].start;
}

